async def main():
    try:
        import edge_tts
    except ImportError:
        print("edge-tts が見つかりません。先に  python -m pip install edge-tts  を実行してください。")
        return 1

    out_dir = Path(__file__).resolve().parent / "audio"
    out_dir.mkdir(parents=True, exist_ok=True)

    ok = 0
    for fname, text in NARRATION:
        out_path = out_dir / fname
        try:
            await edge_tts.Communicate(text, VOICE).save(str(out_path))
            print(f"  生成: {out_path}")
            ok += 1
        except Exception as e:
            print(f"  失敗: {fname} -> {type(e).__name__}: {e}")

    print(f"\n完了: {ok}/{len(NARRATION)} 本を {out_dir} に生成しました。")
    if ok < len(NARRATION):
        print("一部失敗した場合は、ネット接続・社内プロキシ・ウイルス対策の設定をご確認ください。")
    return 0


if __name__ == "__main__":
    # Windows + 新しめのPythonでも動くようにイベントループを明示
    try:
        if sys.platform.startswith("win"):
            asyncio.set_event_loop_policy(asyncio.WindowsSelectorEventLoopPolicy())
    except Exception:
        pass
    raise SystemExit(asyncio.run(main()))
